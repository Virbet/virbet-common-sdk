package org.wireforce.virbet.classes

/**
 * Перечисление, представляющее различные виды спорта.
 */
enum class Sport {
    /**
     * Футбол - командный вид спорта с использованием мяча.
     */
    FOOTBALL,

    /**
     * Баскетбол - командная игра с мячом и корзинами.
     */
    BASKETBALL,

    /**
     * Теннис - индивидуальная или парная игра с мячом и ракеткой.
     */
    TENNIS,

    /**
     * Футбол (американский) - командный вид спорта, известный также как американский футбол.
     */
    SOCCER,

    /**
     * Бейсбол - командный вид спорта с мячом и битой.
     */
    BASEBALL,

    /**
     * Волейбол - командный вид спорта с мячом, перебрасываемым через сетку.
     */
    VOLLEYBALL,

    /**
     * Плавание - индивидуальный или командный вид спорта в воде.
     */
    SWIMMING,

    /**
     * Гольф - игра с мячом и клюшкой, цель которой попасть мячем в лунку.
     */
    GOLF,

    /**
     * Настольный теннис - индивидуальная или парная игра на столе с использованием мяча и ракеток.
     */
    TABLE_TENNIS,

    /**
     * Легкая атлетика - вид спорта, включающий бег, прыжки и метания.
     */
    ATHLETICS,

    HOCKEY,

    FUTSAL,

    HANDBALL,

    ESPORTS,

    BANDY,

    /**
     * Бадминтон - индивидуальный или парный вид спорта с использованием летящего мяча и ракеток.
     */
    BADMINTON,

    /**
     * Велоспорт - вид спорта, включающий гонки на велосипедах различных видов.
     */
    CYCLING,

    /**
     * Бокс - индивидуальный боевой вид спорта с использованием перчаток.
     */
    BOXING,

    /**
     * Хоккей с шайбой - командный вид спорта с использованием клюшек и шайбы.
     */
    ICE_HOCKEY,

    /**
     * Регби - командный вид спорта, сходный с американским футболом.
     */
    RUGBY,

    /**
     * Крикет - командный вид спорта с использованием мяча и биты.
     */
    CRICKET,

    /**
     * Лыжный спорт - вид спорта, включающий гонки на лыжах.
     */
    SKIING,

    /**
     * Борьба - индивидуальный вид спорта, включающий физический контакт между соперниками.
     */
    WRESTLING,

    /**
     * Тяжелая атлетика - вид спорта, включающий метание тяжестей.
     */
    WEIGHTLIFTING,

    /**
     * Гимнастика - индивидуальный или групповой вид спорта, включающий различные упражнения.
     */
    GYMNASTICS,

    /**
     * Стрельба из лука - вид спорта, включающий стрельбу из лука в цель.
     */
    ARCHERY,

    /**
     * Фехтование - индивидуальный вид спорта с использованием клинка.
     */
    FENCING,

    /**
     * Дзюдо - индивидуальный вид боевого искусства, основанный на бросках и ударах.
     */
    JUDO,

    /**
     * Карате - индивидуальный вид боевого искусства с использованием ударов и блоков.
     */
    KARATE,

    /**
     * Гребля - командный вид спорта, включающий греблю на воде.
     */
    ROWING,

    /**
     * Парусный спорт - вид спорта с использованием парусов для движения по воде.
     */
    SAILING,

    /**
     * Триатлон - комбинированный вид спорта, включающий плавание, велосипед и бег.
     */
    TRIATHLON,

    /**
     * Формула-1 - гоночный автоспорт, включающий гонки на специализированных автомобилях.
     */
    FORMULA_ONE,

    /**
     * Смешанные боевые искусства (MMA) - комбинированный бойцовский вид спорта.
     */
    MMA,

    /**
     * Сёрфинг - вид спорта, включающий катание на волнах на доске.
     */
    SURFING,

    /**
     * Скалолазание - вид спорта, включающий восхождение по скалам.
     */
    CLIMBING
}
